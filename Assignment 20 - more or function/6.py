""" Write a python program to create a   function and print a list where the values are square of numbers between 1 and 30. """
print("-----------------------------------------------------------------------------------------------------")
def Squares(n):
     L = [i*i for i in range(1,n+1)]
     return L
print (Squares(30))
print("-----------------------------------------------------------------------------------------------------------")
def squares1(n):
    L= list(range(1,n+1))
    L = [num*num for num in L if num]
    return L
print(squares1(30))    
